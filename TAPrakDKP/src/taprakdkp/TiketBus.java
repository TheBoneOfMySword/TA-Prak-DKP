private void initializeSeatButtons() {
    // Panel untuk kursi di bagian kiri atas
    JPanel leftTopPanel = new JPanel(new GridLayout(11, 2, 5, 10));
    for (int i = 1; i <= 22; i++) {
        String seatNumber = "Kursi " + i;
        JButton seatButton = createSeatButton(seatNumber);
        leftTopPanel.add(seatButton);
    }
    
    // Panel untuk kursi di bagian kanan atas
    JPanel rightTopPanel = new JPanel(new GridLayout(11, 2, 5, 10));
    for (int i = 23; i <= 44; i++) {
        String seatNumber = "Kursi " + i;
        JButton seatButton = createSeatButton(seatNumber);
        rightTopPanel.add(seatButton);
    }

    // Panel untuk kursi di bagian bawah
    JPanel bottomPanel = new JPanel(new GridLayout(1, 5, 5, 10));
    for (int i = 45; i <= 49; i++) {
        String seatNumber = "Kursi " + i;
        JButton seatButton = createSeatButton(seatNumber);
        bottomPanel.add(seatButton);
    }

    // Tambahkan panel-panel ke dalam panel utama
    jPanel1.setLayout(new BorderLayout());
    jPanel1.add(leftTopPanel, BorderLayout.WEST);
    jPanel1.add(rightTopPanel, BorderLayout.EAST);
    jPanel1.add(bottomPanel, BorderLayout.SOUTH);
}

private JButton createSeatButton(String seatNumber) {
    JButton seatButton = new JButton(seatNumber);
    seatButton.setBackground(Color.GREEN);
    seatButton.addActionListener(new SeatButtonListener(seatNumber, seatButton));
    return seatButton;
}


    private class SeatButtonListener implements ActionListener {
        private String seatNumber;
        private JButton seatButton;

        public SeatButtonListener(String seatNumber, JButton seatButton) {
            this.seatNumber = seatNumber;
            this.seatButton = seatButton;
        }

        @Override
        public void actionPerformed(ActionEvent e) {
            if (kursiTerpesan.contains(seatNumber)) {
                kursiTerpesan.remove(seatNumber);
                seatButton.setBackground(Color.GREEN);
            } else {
                int JumlahTiket = (int) jSpinner1.getValue();
                if (kursiTerpesan.size() < JumlahTiket) {
                    kursiTerpesan.add(seatNumber);
                    seatButton.setBackground(Color.RED);
                } else {
                    JOptionPane.showMessageDialog(null, "Jumlah kursi sudah sesuai dengan jumlah tiket", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }


