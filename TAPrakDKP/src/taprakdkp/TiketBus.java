    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nama = jTextField1.getText();
        String destinasi = (String) jComboBox1.getSelectedItem();
        int JumlahTiket = (int) jSpinner1.getValue();

        if (nama.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Masukkan nama anda", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (JumlahTiket > kursiTerpesan.size()) {
            JOptionPane.showMessageDialog(null, "Tolong pilih " + JumlahTiket + " kursi", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            int Hargatotal = getHargaUntukDestinasi(destinasi) * JumlahTiket; // Hitung total harga
            String ticketInfo = "Nama: " + nama + "\nDestinasi: " + destinasi + "\nJumlah Tiket: " + JumlahTiket + "\nHarga Total: Rp" + Hargatotal + "\nNomor Kursi: " + kursiTerpesan + "\n\n";
            jTextArea1.append(ticketInfo);
            jTextField1.setText("");
            jSpinner1.setValue(1);
            kursiTerpesan.clear();
            resetSeatButtons();
            JOptionPane.showMessageDialog(this, "Pesanan telah berhasil dikirim!", "Konfirmasi", JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

